# Exercise 34: Accessing Elements of Arrays

animals = ["bear", "ruby", "peacock", "Kangaroo", "whale", "platypus"]

puts "The first element in the animals array is #{animals[0]}"
puts "The second is %s" % animals[1]
puts "The third element is #{animals[3]}, and the fourth is #{animals[4]}"
puts "The last element in the animals array is #{animals[-1]}"